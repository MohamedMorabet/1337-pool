int	ft_find_next_prime(int nb)
{
	int	i;
	int	res;

	if (nb <= 2)
		return (2);
	while (nb)
	{
		i = 2;
		while (nb % i != 0)
			i++;
		if (nb == i)
			break ;
		nb++;
	}
	return (nb);
}
