void	ft_rev_int_tab(int *tab, int size)
{
	int	a;
	int	i;

	i = 0;
	size -= 1;
	while (i < size)
	{
		a = tab[i];
		tab[i] = tab[size];
		tab[size] = a;
		i++;
		size--;
	}
}
