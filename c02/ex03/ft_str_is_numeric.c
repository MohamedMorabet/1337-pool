int	ft_str_is_numeric(char *str)
{
	int	i;
	int	a;

	i = 0;
	a = 0;
	while (str[i])
	{
		if ((str[i] <= 57 && str[i] >= 48))
			a++;
		i++;
	}
	if (a == i)
		return (1);
	else
		return (0);
}
