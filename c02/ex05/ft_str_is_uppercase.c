int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	a;

	i = 0;
	a = 0;
	while (str[i])
	{
		if (str[i] <= 90 && str[i] >= 65)
			a++;
		i++;
	}
	if (a == i)
		return (1);
	else
		return (0);
}
